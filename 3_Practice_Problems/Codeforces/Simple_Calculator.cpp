#include <bits/stdc++.h>
using namespace std;
long long x,y;
int main ()
{
    cin >> x >> y;
    long long multiply = x*y;

    cout << x << " + " << y << " = " << x+y << endl;
    cout << x << " * " << y << " = " << multiply << endl;
    cout << x << " - " << y << " = " << x-y << endl;

    return 0;
}
